#include <iostream>
 
using namespace std;
 
int main() {
 
    int x, y;
    cout << "Введите первое число:";
    cin >> x;
    cout << "Введите второе число:";
    cin >> y;
 
    cout << "Сумма: " << x + y << endl;
    cout << "Разность: " << x - y << endl;
    cout << "Произведение: " << x * y << endl;
 
    return 0;
}
